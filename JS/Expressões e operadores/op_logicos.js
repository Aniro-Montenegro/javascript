//Operadores logicos

let verdade = true;
let mentira = false;

console.log(verdade && verdade, "verdade && verdade");
console.log(verdade && mentira, "verdade && mentira");
console.log(mentira && mentira, "mentira && mentira");

console.log(mentira || mentira, "mentira || mentira");
console.log(verdade || mentira, "verdade || mentira");
console.log(mentira || verdade, "mentira || verdade");
console.log(mentira || verdade, "mentira || verdade");

console.log(!mentira, "!mentira");
console.log(!verdade, "!verdade");
